unmapped_suppliers = set()

def get_short_supplier_name(long_name):
    """
    Returns the short name for a supplier.
    Checks:
    1. Exact Match
    2. Already Short (Value Check)
    3. Substring Match (Longest Key wins)
    """
    if not long_name: return ""
    long_name = long_name.strip()
    
    # 1. Exact match
    if long_name in SUPPLIER_NAME_MAPPING:
        return SUPPLIER_NAME_MAPPING[long_name]

    # 2. Check if ALREADY short
    if long_name in SUPPLIER_NAME_MAPPING.values():
        return long_name
        
    # 3. Substring Match (Case Insensitive for robustness)
    best_match = ''
    norm_long = long_name.lower()
    
    for key in SUPPLIER_NAME_MAPPING:
        # Check if key is inside long_name (case insensitive)
        if key.lower() in norm_long:
            # We want the longest key match to be specific
            if len(key) > len(best_match):
                best_match = key
    
    if best_match:
        return SUPPLIER_NAME_MAPPING[best_match]
            
    unmapped_suppliers.add(long_name)
    return long_name

def get_telegram_supplier_name(long_name):
    """
    Returns the full name optimized for Telegram reports.
    Converts strictly ALL CAPS text to Title Case, preserving some acronyms.
    """
    if not long_name: return ""
    long_name = long_name.strip()
    
    # Simple Title Casing
    title_cased = long_name.title()
    
    # Fix common acronyms that should remain uppercase or specific case
    fixes = {
        " Pvt ": " Pvt. ",
        " Ltd ": " Ltd. ",
        " Ltd": " Ltd.",
        " Llp ": " LLP ",
        " Imfl ": " IMFL ",
        " Pvt.Ltd.": " Pvt. Ltd.",
        " Cmj ": " CMJ ",
        " Nedpl": " NEDPL",
        " Nefa ": " NEFA ",
        " Pli ": " PLI ",
        " Jil": " JIL",
        " Sstdpl": " SSTDPL",
        " Bbbpl": " BBBPL",
        " Mdh ": " MDH ",
        " M/S ": " M/s "
    }
    
    # We strip out dots first so we have a clean slate for known acronyms
    title_cased = title_cased.replace(".", "")
    
    for old, new in fixes.items():
        title_cased = title_cased.replace(old.replace(".",""), new)
        title_cased = title_cased.replace(old.title().replace(".",""), new)
        title_cased = title_cased.replace(old.lower().replace(".",""), new)
        
    # Extra pass for edge cases at start/end
    if title_cased.endswith(" Pvt"): title_cased = title_cased[:-4] + " Pvt."
    if title_cased.endswith(" Ltd"): title_cased = title_cased[:-4] + " Ltd."
    if title_cased.endswith(" Imfl"): title_cased = title_cased[:-5] + " IMFL"
    
    # Clean up any accidental double dots just in case
    while ".." in title_cased:
        title_cased = title_cased.replace("..", ".")
        
    title_cased = title_cased.replace(" .", ".")
        
    return title_cased


from datetime import datetime
def normalize_date_string(date_str):
    """Converts various date string formats to a standard 'YYYY-MM-DD' format."""
    if not date_str:
        return ''
    possible_formats = ['%d-%b-%Y', '%Y-%m-%d', '%m/%d/%Y', '%d/%m/%Y', '%d/%m/%y']
    for fmt in possible_formats:
        try:
            return datetime.strptime(date_str, fmt).strftime('%Y-%m-%d')
        except ValueError:
            pass
    return date_str.strip()

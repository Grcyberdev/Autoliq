# Keep track of names that are not in our mapping
unmapped_liquor_names = set()

def get_short_name(long_name):
    """Returns the short name from the mapping, or the original name if not found."""
    # Normalize scraped name to match dictionary keys
    long_name = long_name.strip().replace("'", "`")
    
    # Check    # If exact match found
    if long_name in LIQUOR_NAME_MAPPING:
        return LIQUOR_NAME_MAPPING[long_name]
        
    # Check if it's ALREADY a short name (exists in values)
    # Check normalized values (case and punctuation insensitive)
    def normalize_for_check(s):
        return s.lower().strip().replace("'", "").replace("`", "")
    
    norm_long = normalize_for_check(long_name)
    
    for val in LIQUOR_NAME_MAPPING.values():
        if normalize_for_check(val) == norm_long:
            return val

    # Normalize for case-insensitive search
    # (Original normalization which keeps backticks for key matching)
    normalized_name_key = long_name.lower().strip()
    
    # Try case-insensitive lookup (Keys)
    for key, value in LIQUOR_NAME_MAPPING.items():
        if key.lower().strip() == normalized_name_key:
            return value

    # If still no match, report it and return the original
    if long_name: # Don't add empty strings
        unmapped_liquor_names.add(long_name)
    return long_name


